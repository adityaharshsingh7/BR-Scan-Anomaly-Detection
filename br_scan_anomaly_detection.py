def visualize_result(original_bgr, anomaly_map, filename,
                     diagnosis, anomaly_pct, confidence, save_path=None):
    """
    Four-panel output:
    [Original Scan] [Anomaly Map] [Overlay] [Diagnosis Report]
    """
    original_rgb = cv2.cvtColor(
        cv2.resize(original_bgr, CONFIG["target_size"]),
        cv2.COLOR_BGR2RGB
    )

    heatmap_color = cv2.applyColorMap(anomaly_map, cv2.COLORMAP_HOT)
    heatmap_rgb   = cv2.cvtColor(heatmap_color, cv2.COLOR_BGR2RGB)
    overlay       = cv2.addWeighted(original_rgb, 0.6, heatmap_rgb, 0.4, 0)

    fig, axes = plt.subplots(1, 4, figsize=(20, 5))
    fig.patch.set_facecolor('#0d0d0d')
    title_color = '#ff4444' if diagnosis == 'MALIGNANT' else '#44ff88'
    fig.suptitle(f"D3S BR-Scan  ·  {filename}", fontsize=12,
                 fontweight='bold', color='white')

    for ax in axes:
        ax.set_facecolor('#1a1a1a')

    axes[0].imshow(original_rgb)
    axes[0].set_title("Original Scan", color='white', fontsize=10)
    axes[0].axis('off')

    axes[1].imshow(anomaly_map, cmap='hot')
    axes[1].set_title(f"Anomaly Map  ({anomaly_pct:.1f}%)", color='white', fontsize=10)
    axes[1].axis('off')

    axes[2].imshow(overlay)
    axes[2].set_title("Overlay", color='white', fontsize=10)
    axes[2].axis('off')

    # Diagnosis panel
    verdict_symbol = "⚠️  MALIGNANT" if diagnosis == "MALIGNANT" else "✅  BENIGN"
    report = (
        f"{verdict_symbol}\n\n"
        f"Anomaly Score : {anomaly_pct:.1f}%\n"
        f"Threshold     : {CONFIG['anomaly_threshold_pct']:.1f}%\n\n"
        f"━━━━━━━━━━━━━━━━━━\n"
        f"Confidence    : {confidence['composite']*100:.1f}%\n"
        f"Grade         : {confidence['grade']}\n\n"
        f"  Margin      : {confidence['margin']*100:.0f}%\n"
        f"  Coherence   : {confidence['coherence']*100:.0f}%\n"
        f"  Skew        : {confidence['skew']*100:.0f}%"
    )
    axes[3].text(0.08, 0.92, report,
                 transform=axes[3].transAxes,
                 fontsize=9.5, color=title_color,
                 fontweight='bold', verticalalignment='top',
                 fontfamily='monospace')
    axes[3].set_title("Diagnosis", color='white', fontsize=10)
    axes[3].axis('off')

    plt.tight_layout()
    if save_path:
        plt.savefig(save_path, dpi=100, bbox_inches='tight',
                    facecolor=fig.get_facecolor())
    plt.show()
    plt.close()


