def run_diagnostic_demo(folder_path, model, device):
    class_labels = ['benign', 'malignant', 'normal']
    stats = {label: 0 for label in class_labels}

    files = [f for f in os.listdir(folder_path) if f.lower().endswith(('.jpg', '.png'))]
    print(f"✅ Dataset LOADED: Found {len(files)} scans. Starting Batch Analysis...")

    for filename in tqdm(files, desc="Analyzing Scans"):
        path = os.path.join(folder_path, filename)
        input_tensor, raw_img = advanced_shadow_preprocessor(path, device)
        if input_tensor is None: continue

        # Grad-CAM Heatmap Hook
        features = []
        def hook_fn(module, input, output): features.append(output)
        handle = model.layer4[-1].register_forward_hook(hook_fn)
        output = model(input_tensor)
        handle.remove()

        probs = F.softmax(output, dim=1)
        conf, idx = torch.max(probs, 1)
        stats[class_labels[idx]] += 1

        # Heatmap Generation
        f_map = features[0].detach().cpu().numpy()[0]
        heatmap = np.mean(f_map, axis=0)
        heatmap = np.maximum(heatmap, 0)
        heatmap /= (np.max(heatmap) + 1e-7)
        heatmap = cv2.resize(heatmap, (raw_img.shape[1], raw_img.shape[0]))
        heatmap = np.uint8(255 * heatmap)
        heatmap = cv2.applyColorMap(heatmap, cv2.COLORMAP_JET)
        overlay = cv2.addWeighted(raw_img, 0.6, heatmap, 0.4, 0)

        # Plotting
        plt.figure(figsize=(10, 4))
        plt.subplot(1, 2, 1); plt.imshow(raw_img); plt.title(f"Original: {filename}"); plt.axis('off')
        plt.subplot(1, 2, 2); plt.imshow(overlay); plt.title(f"{class_labels[idx].upper()} ({conf.item()*100:.1f}%)"); plt.axis('off')
        plt.show()

    # Final Report Card
    total = sum(stats.values())
    print("\n" + "="*45)
    print("         D3S BATCH DIAGNOSTIC SUMMARY         ")
    print("="*45)
    for label, count in stats.items():
        pct = (count/total)*100 if total > 0 else 0
        print(f"{label.upper():<12}: {count:>3} images ({pct:>5.1f}%)")
    print("="*45)

