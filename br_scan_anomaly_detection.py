def compute_anomaly_map(scan_gray: np.ndarray) -> tuple:
    """
    Core detection logic:
    1. Align scan to master profile (corrects positioning variance)
    2. Directional subtraction: master_healthy - scan
       → Positive values = scan is DARKER than healthy = dense tissue shadow
       → absdiff() is WRONG here — it also flags scan-brighter regions (artifacts)
    3. Apply nipple mask
    4. Threshold + morphological cleaning
    Returns (anomaly_map, mask_method_used)
    """
    scan_f32 = scan_gray.astype(np.float32)
    scan_aligned = align_to_reference(scan_f32, master_profile)

    # Directional subtraction — physics-correct for transillumination
    diff = np.clip(master_profile - scan_aligned, 0, 255).astype(np.uint8)

    # Nipple masking (Hough-based)
    diff_masked, mask_method = apply_dynamic_nipple_mask(diff)

    # Threshold significant density deviations
    _, thresh = cv2.threshold(
        diff_masked, CONFIG["diff_threshold"], 255, cv2.THRESH_BINARY
    )

    # Remove isolated noise pixels
    kernel = np.ones((5, 5), np.uint8)
    anomaly_map = cv2.morphologyEx(thresh, cv2.MORPH_OPEN, kernel)

    return anomaly_map, mask_method


def classify_scan(anomaly_map: np.ndarray) -> tuple:
    """
    Returns (diagnosis, anomaly_percentage).
    Threshold comes from CONFIG — not hardcoded here.
    """
    total = anomaly_map.shape[0] * anomaly_map.shape[1]
    count = np.count_nonzero(anomaly_map)
    pct   = (count / total) * 100
    diag  = "MALIGNANT" if pct >= CONFIG["anomaly_threshold_pct"] else "BENIGN"
    return diag, pct


def compute_confidence(anomaly_map: np.ndarray, anomaly_pct: float) -> dict:
    """
    Three-component confidence score [0.0 → 1.0]:

    1. Margin    — how far above/below the threshold is the anomaly score?
    2. Coherence — are anomalous pixels a contiguous mass (real tissue)
                   or scattered noise? High coherence = likely real mass.
    3. Skew      — does the deviation map have a heavy right tail?
                   Dense tissue creates concentrated high-intensity deviations.
    """
    threshold = CONFIG["anomaly_threshold_pct"]

    # 1. Margin (sigmoid centered at threshold)
    margin = float(1 / (1 + np.exp(-0.3 * (anomaly_pct - threshold))))

    # 2. Spatial coherence (largest connected blob / total anomalous pixels)
    binary = (anomaly_map > 0).astype(np.uint8)
    total_anomalous = int(binary.sum())
    if total_anomalous == 0:
        coherence = 0.0
    else:
        n_labels, _, cc_stats, _ = cv2.connectedComponentsWithStats(binary)
        if n_labels < 2:
            coherence = 0.0
        else:
            largest = int(cc_stats[1:, cv2.CC_STAT_AREA].max())
            coherence = float(largest / total_anomalous)

    # 3. Distribution skewness
    skewness   = float(scipy_stats.skew(anomaly_map.flatten().astype(np.float32)))
    skew_score = float(np.clip(skewness / 3.0, 0.0, 1.0))

    composite = (
        CONFIG["conf_weight_margin"]    * margin    +
        CONFIG["conf_weight_coherence"] * coherence +
        CONFIG["conf_weight_skew"]      * skew_score
    )

    grade = (
        "HIGH"     if composite >= 0.85 else
        "MODERATE" if composite >= 0.60 else
        "LOW"      if composite >= 0.40 else
        "BENIGN"
    )

    return {
        "composite": round(composite, 3),
        "margin":    round(margin, 3),
        "coherence": round(coherence, 3),
        "skew":      round(skew_score, 3),
        "grade":     grade
    }


