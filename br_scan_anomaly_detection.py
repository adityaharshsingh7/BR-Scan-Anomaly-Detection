def align_to_reference(image: np.ndarray, reference: np.ndarray) -> np.ndarray:
    """
    Aligns a scan to the master profile using ECC (Enhanced Correlation Coefficient).
    Fixes small translation/rotation differences between captures.
    Falls back silently if alignment fails.
    """
    warp_matrix = np.eye(2, 3, dtype=np.float32)
    criteria = (cv2.TERM_CRITERIA_EPS | cv2.TERM_CRITERIA_COUNT, 200, 1e-7)
    try:
        _, warp_matrix = cv2.findTransformECC(
            reference.astype(np.float32),
            image.astype(np.float32),
            warp_matrix,
            cv2.MOTION_EUCLIDEAN,
            criteria
        )
        h, w = reference.shape
        aligned = cv2.warpAffine(
            image, warp_matrix, (w, h),
            flags=cv2.INTER_LINEAR + cv2.WARP_INVERSE_MAP
        )
        return aligned
    except cv2.error:
        return image  # ECC failed — return unaligned rather than crash


def apply_dynamic_nipple_mask(image_gray: np.ndarray) -> tuple:
    """
    V5 FIX: Restored Hough-based nipple detection (replaces V4's fixed center mask).
    The fixed center mask breaks when the nipple is off-center.
    Hough detection finds the actual nipple location regardless of patient positioning.
    Falls back to fixed center mask only if no circle is detected.
    Returns (masked_image, detection_method_used)
    """
    h, w = image_gray.shape[:2]
    blurred = cv2.medianBlur(image_gray, 5)

    circles = cv2.HoughCircles(
        blurred,
        cv2.HOUGH_GRADIENT,
        dp=CONFIG["hough_dp"],
        minDist=CONFIG["hough_min_dist"],
        param1=CONFIG["hough_param1"],
        param2=CONFIG["hough_param2"],
        minRadius=CONFIG["hough_min_radius"],
        maxRadius=CONFIG["hough_max_radius"]
    )

    mask = np.ones((h, w), dtype=np.uint8) * 255

    if circles is not None:
        circles = np.round(circles[0, :]).astype("int")
        x, y, r = circles[0]  # most prominent circle only
        cv2.circle(mask, (x, y), r + CONFIG["nipple_mask_padding"], 0, -1)
        method = "hough"
    else:
        # Fallback only — logged so you can audit which scans used it
        fallback_r = int(w * CONFIG["fallback_mask_radius"])
        cv2.circle(mask, (w // 2, h // 2), fallback_r, 0, -1)
        method = "fallback_center"

    masked = cv2.bitwise_and(image_gray, image_gray, mask=mask)
    return masked, method


def preprocess_scan(image_path: str) -> tuple:
    """
    Loads and preprocesses a single scan.
    Returns (gray_float32, original_bgr) or (None, None) on load failure.
    """
    img_bgr = cv2.imread(image_path)
    if img_bgr is None:
        return None, None

    gray = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2GRAY)
    gray = cv2.resize(gray, CONFIG["target_size"])
    gray = cv2.GaussianBlur(gray, CONFIG["blur_kernel"], 0)
    return gray, img_bgr


