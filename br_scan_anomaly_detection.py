# --- SECTION 2: DYNAMIC PREPROCESSING & NIPPLE MASKING ---

def apply_dynamic_nipple_mask(image_cv):
    """
    Detects the nipple using Hough Circle Transform and masks it
    to prevent anatomical density from causing False Positives.
    """
    gray = cv2.cvtColor(image_cv, cv2.COLOR_RGB2GRAY)
    blurred = cv2.medianBlur(gray, 5)

    # Hough Circles: Adjusted to find the circular density of the nipple
    circles = cv2.HoughCircles(blurred, cv2.HOUGH_GRADIENT, dp=1.2, minDist=100,
                               param1=50, param2=30, minRadius=20, maxRadius=65)

    masked_img = image_cv.copy()
    if circles is not None:
        circles = np.round(circles[0, :]).astype("int")
        for (x, y, r) in circles:
            # Masking the nipple area with absolute black
            cv2.circle(masked_img, (x, y), r + 15, (0, 0, 0), -1)
    return masked_img

def load_diagnostic_model(checkpoint_path, num_classes=3):
    print(f"🔄 STEP 2: VERIFYING RESNET-50 MODEL FILE...")
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    model = models.resnet50(weights=None)
    model.fc = nn.Linear(model.fc.in_features, num_classes)

    if os.path.exists(checkpoint_path):
        model.load_state_dict(torch.load(checkpoint_path, map_location=device))
        model.to(device).eval()
        print(f"✅ SUCCESS: Model loaded on {str(device).upper()}.")
        return model, device
    else:
        print(f"❌ ERROR: Model file NOT FOUND at: {checkpoint_path}")
        return None, None

def advanced_shadow_preprocessor(image_path, device):
    """
    Isolates red spectrum, applies dynamic masking, and sharpens density shadows.
    """
    img = cv2.imread(image_path)
    if img is None: return None, None
    img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

    # 1. Dynamic Nipple Suppression
    img = apply_dynamic_nipple_mask(img)

    # 2. Spectral Enhancement
    red_channel = img[:,:,0]
    # clipLimit=10.0 to amplify subtle malignant shadows
    clahe = cv2.createCLAHE(clipLimit=10.0, tileGridSize=(8,8))
    enhanced_red = clahe.apply(red_channel)

    final_img = np.stack([enhanced_red]*3, axis=-1)

    transform = transforms.Compose([
        transforms.ToPILImage(),
        transforms.Resize((224, 224)),
        transforms.ToTensor(),
        transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
    ])
    return transform(final_img).unsqueeze(0).to(device), img

